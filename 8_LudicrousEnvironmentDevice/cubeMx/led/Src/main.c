{

  /* USER CODE BEGIN 1 */
  CAN_msg_t msg;
  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  SYSTICK_init(&(global.absolute_time));
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_LTDC_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();

  /* USER CODE BEGIN 2 */
  HAL_TIM_Base_Start_IT(&htim3);
  HAL_TIM_Base_Start_IT(&htim4);

  UART_init(UART_DEBUG, UART_DEBUG_BAUDRATE);
  UART_init(UART_XBEE, UART_XBEE_BAUDRATE);

  CAN_over_XBee_init(LED_MODULE, BIG_ROBOT_MODULE);
  XBEE_ping_pong_enable(FALSE);

  debug_printf("\n------- Hello, I'm LED -------\n");

  INTERFACE_init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */

	  ENV_process_main();

	  CAN_over_XBee_process_main();

	  // Traitement des messages XBee
	  while(XBeeToCANmsg(&msg)){
		    HAL_GPIO_TogglePin(red_led_GPIO_Port, red_led_Pin);
			CAN_process_msg(&msg);		// Traitement du message pour donner les consignes à la machine d'état
			#ifdef CAN_VERBOSE_MODE
				QS_CAN_VERBOSE_can_msg_print(&msg);
			#endif
		}

	  INTERFACE_processMain();

  }
  /* USER CODE END 3 */

}

